function compareIds(a, b) {
  const pa = parts(a.id);
  const pb = parts(b.id);
  if (pa.kind !== pb.kind) return order(pa.kind) - order(pb.kind);
  return pa.num - pb.num;
}

function parts(id) {
  const match = id.match(/-(q|s|r)(\d+)$/);
  return { kind: match?.[1] ?? 'z', num: Number(match?.[2] ?? 9999) };
}

function order(kind) {
  return { q: 1, s: 2, r: 3, z: 4 }[kind] ?? 4;
}
